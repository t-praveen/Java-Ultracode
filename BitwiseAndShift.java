//PROGRAM FOR BITWISE SHIFTING AND OPERTATORS//

package ultracode;

public class BitwiseAndShift {

	public static void main(String[] args) {
		
		int x = 9, y = 8; 
		int a = 50 ;
		System.out.println("x & y = " + (x & y)); 
		System.out.println("x ^ y = " + (x ^ y)); 
		System.out.println("x | y = " + (x | y));   
		System.out.println("a>>2 = " + (a >>2));
		System.out.println("a<<1 = " + (a << 1));
		

	}

}
